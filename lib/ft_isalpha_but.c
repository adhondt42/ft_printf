#include "../inc/ft_printf.h"

int	ft_isalpha_but(int c, char *str)
{
	if (((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
			&& (pos_in_str(c, str) < 0))
		return (1);
	else
		return (0);
}

#include "../inc/ft_printf.h"

int	ft_strlen(const char *src)
{
	int		i;
	char	*str;

	if (!src)
		return (0);
	str = (char *)src;
	i = 0;
	while (src[i] != '\0')
		i++;
	return (i);
}
